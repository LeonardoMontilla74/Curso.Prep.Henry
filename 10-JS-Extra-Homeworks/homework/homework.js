  //en formato par clave-valor.
  //Ej: Recibe ---> "adsjfdsfsfjsdjfhacabcsbajda" || Devuelve ---> { a: 5, b: 2, c: 2, d: 4, f: 4, h:1, j: 4, s: 5 } 
  //Escribe tu código aquí
  let letters = {};
  for (let i = 0; i < string.length; i++) {
    if (Object.keys(letters).includes(string[i])) {
     letters[string[i]] += 1;
    } else {
      letters[string[i]] = 1
    }
  }
